#   Exercício Python 003 - Somando dois números
#   Crie um programa que leia dois números e mostre a soma entre eles.

nr1 = int(input('Digite o primeiro número: '))
nr2 = int(input ('Digite o segundo número: '))
print("A soma entre {} e {} é igual a {}!".format(nr1, nr2, nr1+nr2))
print(f'A soma entre {nr1} e {nr2} é igual a {nr1 + nr2}!')
